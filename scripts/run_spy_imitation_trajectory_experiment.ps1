param(
    [string]$Profile = "Pauper-Spy-Combo-Imitation-20260502",
    [string]$RunId = "",
    [int]$TargetTrajectories = 10000,
    [double]$MaxHours = 24.0,
    [int]$BatchScenarios = 16,
    [int]$Workers = 12,
    [int]$AiThreads = 24,
    [int]$HandPoolCount = 256,
    [int]$Seed = 6101,
    [string]$EvalMilestones = "256,1000,2500,5000,10000",
    [switch]$SkipCompile
)

$ErrorActionPreference = "Stop"

$repo = (Resolve-Path (Join-Path $PSScriptRoot "..")).Path
if ([string]::IsNullOrWhiteSpace($RunId)) {
    $RunId = "spy_imitation_" + (Get-Date -Format "yyyyMMdd_HHmmss")
}
$root = Join-Path $repo ("local-training\local_pbt\spy_imitation_trajectory\" + $RunId)
$handsDir = Join-Path $root "hand_pools"
$evalDir = Join-Path $root "evals"
New-Item -ItemType Directory -Force -Path $root, $handsDir, $evalDir | Out-Null

$summaryCsv = Join-Path $root "batch_summary.csv"
$registryPath = Join-Path $root "eval_registry.json"
$activePool = "Mage.Server.Plugins/Mage.Player.AIRL/src/mage/player/ai/decks/Pauper/decklist.active_profile_pool.txt"
$spyDeck = "Mage.Server.Plugins/Mage.Player.AIRL/src/mage/player/ai/decks/Pauper/Deck - Spy Combo.dek"

if (-not (Test-Path $summaryCsv)) {
    "timestamp,batch,run_id,batch_seconds,trajectories,cumulative_trajectories,candidate_examples,tensor_top1,tensor_total,tensor_accuracy" |
        Set-Content -Path $summaryCsv -Encoding UTF8
}

$registry = @(
    [ordered]@{
        profile = $Profile
        deck_path = $activePool
        active = $true
        train_enabled = $false
        target_winrate = 0.70
        priority = 0
        population_group = "spy-imitation"
        seed = $Seed
        train_env = [ordered]@{
            RL_AGENT_DECK_LIST = $spyDeck
            MODEL_D_MODEL = "128"
            MODEL_NUM_LAYERS = "2"
            AI_MAX_THREADS_FOR_SIMULATIONS = [string]$AiThreads
            EVAL_CP7_SKILL = "1"
            RL_RANDOM_DECISIONS = "0"
            MCTS_TRAINING_ENABLE = "0"
            ISMCTS_ENABLE = "0"
            ISMCTS_ROLLOUTS_PER_TURN = "0"
        }
        notes = "Pure behavior cloning from Spy combo milestone trajectories generated by ActionCounterfactualTrainer."
    }
)
$registryJson = ConvertTo-Json -InputObject $registry -Depth 8
[System.IO.File]::WriteAllText($registryPath, $registryJson, [System.Text.UTF8Encoding]::new($false))

if (-not $SkipCompile) {
    & mvn -q -pl Mage.Server.Plugins/Mage.Player.AIRL -am -DskipTests compile
    if ($LASTEXITCODE -ne 0) {
        throw "compile failed with exit code $LASTEXITCODE"
    }
}

function Read-KeyValue([string]$Path) {
    $map = @{}
    if (-not (Test-Path $Path)) {
        return $map
    }
    foreach ($line in Get-Content $Path) {
        if ($line -match '^([^:]+):\s*(.*)$') {
            $map[$matches[1].Trim()] = $matches[2].Trim()
        }
    }
    return $map
}

function Read-TensorSummary([string]$Path) {
    $result = @{ top1 = 0; total = 0; accuracy = 0.0 }
    if (-not (Test-Path $Path)) {
        return $result
    }
    $kv = Read-KeyValue $Path
    if ($kv.ContainsKey("top1_matches")) { $result.top1 = [int]$kv["top1_matches"] }
    if ($kv.ContainsKey("records")) { $result.total = [int]$kv["records"] }
    if ($kv.ContainsKey("top1_accuracy")) { $result.accuracy = [double]$kv["top1_accuracy"] }
    return $result
}

function Run-Eval([int]$TrajectoryCount, [int]$GamesPerMatchup) {
    $evalRunId = "$RunId`_cp1_$TrajectoryCount"
    $evalOut = Join-Path $evalDir $evalRunId
    New-Item -ItemType Directory -Force -Path $evalOut | Out-Null
    $logPath = Join-Path $evalOut "eval.log"
    & py -3 scripts/run_cp7_eval_sweep.py `
        --registry $registryPath `
        --profiles $Profile `
        --skill 1 `
        --games-per-matchup $GamesPerMatchup `
        --games-per-job 8 `
        --parallel 4 `
        --ai-threads 8 `
        --run-id $evalRunId `
        --skip-compile *>&1 | Tee-Object -FilePath $logPath
}

$milestones = @()
foreach ($token in $EvalMilestones.Split(",")) {
    $trimmed = $token.Trim()
    if ($trimmed.Length -gt 0) {
        $milestones += [int]$trimmed
    }
}
$completedMilestones = New-Object System.Collections.Generic.HashSet[int]
$started = Get-Date
$deadline = $started.AddHours($MaxHours)
$cumulative = 0
$batch = 0

while ($cumulative -lt $TargetTrajectories -and (Get-Date) -lt $deadline) {
    $batch++
    $batchStart = Get-Date
    $batchRunId = "$RunId`_batch" + $batch.ToString("0000")
    $handPath = Join-Path $handsDir ("hands_batch" + $batch.ToString("0000") + ".txt")
    $batchSeed = $Seed + (1009 * $batch)

    & py -3 scripts/generate_spy_reachable_hand_pool.py --out $handPath --count $HandPoolCount --seed $batchSeed
    if ($LASTEXITCODE -ne 0) {
        throw "hand pool generation failed for batch $batch"
    }

    $failed = $false
    try {
        & ./scripts/run_spy_line_search.ps1 `
            -Profile $Profile `
            -RunId $batchRunId `
            -AgentOpeningHandPoolFile $handPath `
            -Scenarios $BatchScenarios `
            -BatchSize 128 `
            -Opponent cp7 `
            -Cp7Skill 1 `
            -ModelDModel 128 `
            -ModelNumLayers 2 `
            -ReportEvery 4 `
            -Workers $Workers `
            -AiThreads $AiThreads `
            -MaxPrefixDepth 16 `
            -TrainPrefixDepth 24 `
            -MaxSearchNodes 350 `
            -MaxWinningPrefixesPerScenario 1 `
            -TopK 2 `
            -RandomExtra 0 `
            -TrainEpochs 2 `
            -CandidatePermutations 1 `
            -TerminalMode SPY_COMBO_MILESTONE_ONLY `
            -MaxGameTurns 10 `
            -TimeoutSec 180 `
            -ScenarioTimeoutSec 120 `
            -TacticAutopilot `
            -NoSearchModelScoring `
            -RandomDecisions `
            -SkipCompile
        if ($LASTEXITCODE -ne 0) {
            $failed = $true
        }
    } catch {
        $failed = $true
        Write-Host "spy_imitation_batch_failed batch=$batch run=$batchRunId error=$($_.Exception.Message)"
    }
    if ($failed) {
        $seconds = [Math]::Round(((Get-Date) - $batchStart).TotalSeconds, 1)
        $timestamp = (Get-Date).ToString("s")
        "$timestamp,$batch,$batchRunId,$seconds,0,$cumulative,0,0,0,0" |
            Add-Content -Path $summaryCsv -Encoding UTF8
        Start-Sleep -Seconds 15
        continue
    }

    $batchOut = Join-Path $repo ("local-training\local_pbt\spy_line_search\" + $batchRunId)
    $readme = Read-KeyValue (Join-Path $batchOut "README.md")
    $tensor = Read-TensorSummary (Join-Path $batchOut "tensor_replay_summary.md")
    $traj = if ($readme.ContainsKey("winning_trajectories")) { [int]$readme["winning_trajectories"] } else { 0 }
    $examples = if ($readme.ContainsKey("candidate_examples")) { [int]$readme["candidate_examples"] } else { 0 }
    $cumulative += $traj
    $seconds = [Math]::Round(((Get-Date) - $batchStart).TotalSeconds, 1)
    $timestamp = (Get-Date).ToString("s")
    "$timestamp,$batch,$batchRunId,$seconds,$traj,$cumulative,$examples,$($tensor.top1),$($tensor.total),$($tensor.accuracy)" |
        Add-Content -Path $summaryCsv -Encoding UTF8
    Write-Host "spy_imitation_progress batch=$batch trajectories=$traj cumulative=$cumulative seconds=$seconds tensor_top1=$($tensor.top1)/$($tensor.total)"

    foreach ($milestone in $milestones) {
        if ($cumulative -ge $milestone -and -not $completedMilestones.Contains($milestone)) {
            [void]$completedMilestones.Add($milestone)
            Run-Eval -TrajectoryCount $cumulative -GamesPerMatchup 16
        }
    }
}

if ($cumulative -gt 0) {
    Run-Eval -TrajectoryCount $cumulative -GamesPerMatchup 32
}

Write-Host "spy_imitation_done run=$RunId profile=$Profile trajectories=$cumulative summary=$summaryCsv"
